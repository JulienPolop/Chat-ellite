    void Start()
    {
        
    }

    // Update is called once per frame
    void Update()
    {
        
    }

    private void OnCollisionEnter2D(Collision2D collision)
    {
        if (collision != null)
        {
            Debug.Log("Proj dest"+ collision.gameObject.name);
            Destroy(this.gameObject);
        }
    }
}
